class ReLU(TensorOp):
    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        copy = array_api.copy(a)
        copy[copy < 0] = 0
        return copy
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        copy = array_api.copy(node.realize_cached_data())
        copy[copy > 0] = 1
        copy[copy < 0] = 0
        return out_grad * Tensor(copy)
        ### END YOUR SOLUTION


